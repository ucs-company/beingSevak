function getDateValue(dateStr) {
  const months = {
    Jan: '01', Feb: '02', Mar: '03', Apr: '04', May: '05', Jun: '06',
    Jul: '07', Aug: '08', Sep: '09', Oct: '10', Nov: '11', Dec: '12'
  };
  const parts = dateStr.split(' ');
  if (parts.length === 3) {
    const d = parts[0].padStart(2, '0');
    const m = months[parts[1]] || '01';
    const y = parts[2];
    return `${y}-${m}-${d}`;
  }
  return `${parts[parts.length - 1]}-01-01`;
}

function renderPress() {
  const search = document.getElementById('pressSearch').value.toLowerCase().trim();
  const yearFilter = document.getElementById('pressFilter').value;
  const grid = document.getElementById('pressGrid');

  let filtered = pressData.filter(item => {
    if (yearFilter !== 'all' && item.year !== parseInt(yearFilter)) return false;
    if (search) {
      const t = item.title.toLowerCase();
      const d = item.description.toLowerCase();
      const s = item.source.toLowerCase();
      const date = item.date.toLowerCase();
      if (!t.includes(search) && !d.includes(search) && !s.includes(search) && !date.includes(search)) return false;
    }
    return true;
  });

  if (filtered.length === 0) {
    grid.innerHTML = `
      <div class="press-empty">
        <i class="fas fa-newspaper"></i>
        <p>No press releases match your criteria.</p>
      </div>`;
    return;
  }

  grid.innerHTML = filtered.map((item, i) => `
    <div class="press-card" style="animation-delay:${(i % 14) * 0.05}s">
      <div class="press-card-img-wrap">
        <img src="${item.image}" alt="${item.title}" loading="lazy" onerror="this.parentElement.innerHTML='<div style=\\'height:200px;background:linear-gradient(135deg,#03163E,#00A3DA);display:flex;align-items:center;justify-content:center;color:#fff;font-size:40px;font-weight:800;\\'>${item.title.charAt(0)}</div>'">
      </div>
      <div class="press-card-body">
        <div class="press-card-date">
          <i class="far fa-calendar-alt"></i> ${item.date}
        </div>
        <h3 class="press-card-title">${item.title}</h3>
        <p class="press-card-desc">${item.description}</p>
        <div class="press-card-footer">
          <span class="press-card-source">${item.source}</span>
          <a href="${item.link}" target="_blank" rel="noopener noreferrer" class="press-card-link">
            Read More <i class="fas fa-arrow-right"></i>
          </a>
        </div>
      </div>
    </div>
  `).join('');
}

document.addEventListener('DOMContentLoaded', renderPress);
